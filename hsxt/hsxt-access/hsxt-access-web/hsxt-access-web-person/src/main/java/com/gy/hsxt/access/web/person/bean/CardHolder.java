package com.gy.hsxt.access.web.person.bean;

public class CardHolder {
	/** 互生号 **/
	private String resNo;
	/** 客户号 **/
	private String custId;
	/** 姓名 **/
	private String fullName;
	/** 国籍 **/
	private String nationality;
	/** 证件类型 **/
	private String idType;
	/** 证件号码 **/
	private String idNO;
	/** 职业 **/
	private String job;
	/** 证件有效期 **/
	private String validUntil;
	/** 发证机关 **/
	private String issuingOrgan;
	/** 户籍地址 **/
	private String residentAddress;
	/** 证件正面 **/
	private String certificateFront;
	/** 证件反面 **/
	private String certificateOpposite;
	/** 手持证件照 **/
	private String certificateHandheld;
	/** 性别 **/
	private String sex;
	/** 变更原因 **/
	private String appReason;
	/** 是否默认 **/
	private String licenceIssuing;
	
	public String getResNo() {
		return resNo;
	}
	public void setResNo(String resNo) {
		this.resNo = resNo;
	}
	public String getFullName() {
		return fullName;
	}
	public void setFullName(String fullName) {
		this.fullName = fullName;
	}
	public String getNationality() {
		return nationality;
	}
	public void setNationality(String nationality) {
		this.nationality = nationality;
	}
	public String getIdType() {
		return idType;
	}
	public void setIdType(String idType) {
		this.idType = idType;
	}
	public String getIdNO() {
		return idNO;
	}
	public void setIdNO(String idNO) {
		this.idNO = idNO;
	}
	public String getJob() {
		return job;
	}
	public void setJob(String job) {
		this.job = job;
	}
	public String getValidUntil() {
		return validUntil;
	}
	public void setValidUntil(String validUntil) {
		this.validUntil = validUntil;
	}
	public String getIssuingOrgan() {
		return issuingOrgan;
	}
	public void setIssuingOrgan(String issuingOrgan) {
		this.issuingOrgan = issuingOrgan;
	}
	public String getResidentAddress() {
		return residentAddress;
	}
	public void setResidentAddress(String residentAddress) {
		this.residentAddress = residentAddress;
	}
	public String getCertificateFront() {
		return certificateFront;
	}
	public void setCertificateFront(String certificateFront) {
		this.certificateFront = certificateFront;
	}
	public String getCertificateOpposite() {
		return certificateOpposite;
	}
	public void setCertificateOpposite(String certificateOpposite) {
		this.certificateOpposite = certificateOpposite;
	}
	public String getCertificateHandheld() {
		return certificateHandheld;
	}
	public void setCertificateHandheld(String certificateHandheld) {
		this.certificateHandheld = certificateHandheld;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getCustId() {
		return custId;
	}
	public void setCustId(String custId) {
		this.custId = custId;
	}
	public String getAppReason() {
		return appReason;
	}
	public void setAppReason(String appReason) {
		this.appReason = appReason;
	}
	public String getLicenceIssuing() {
		return licenceIssuing;
	}
	public void setLicenceIssuing(String licenceIssuing) {
		this.licenceIssuing = licenceIssuing;
	}
	
}
